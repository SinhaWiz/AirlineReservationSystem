import java.util.Scanner;

public class CustomerData {
    public String name;
    public String email;
    public String password;
    public String phone;
    public String address;
    public int age;

    public CustomerData(String name, String email, String password, String phone, String address, int age) {
        this.name = name;
        this.email = email;
        this.password = password;
        this.phone = phone;
        this.address = address;
        this.age = age;
    }

}
